package edunova.model.vjezbanje.osnovno;

import java.math.BigDecimal;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;

/**
 *
 * @author Admin
 */
@Entity(name = "racunala")
public class Racunalo {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Integer sifra;
    @Column(columnDefinition = "varchar(55)")
    private String naziv;
    private String opis;
    private boolean ispravan;
    private BigDecimal cijena;

    public Integer getSifra() {
        return sifra;
    }

    public void setSifra(Integer sifra) {
        this.sifra = sifra;
    }

    public String getNaziv() {
        return naziv;
    }

    public void setNaziv(String naziv) {
        this.naziv = naziv;
    }

    public String getOpis() {
        return opis;
    }

    public void setOpis(String opis) {
        this.opis = opis;
    }

    public boolean isIspravan() {
        return ispravan;
    }

    public void setIspravan(boolean ispravan) {
        this.ispravan = ispravan;
    }

    public BigDecimal getCijena() {
        return cijena;
    }

    public void setCijena(BigDecimal cijena) {
        this.cijena = cijena;
    }
    
    
}
